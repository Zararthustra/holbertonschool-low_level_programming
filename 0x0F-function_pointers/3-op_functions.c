#include "3-calc.h"

/**
 * op_add - add a b
 * @a: int a
 * @b: int b
 * Return: result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract a b
 * @a: int a
 * @b: int b
 * Return: result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply a b
 * @a: int a
 * @b: int b
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide a b
 * @a: int a
 * @b: int b
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo a b
 * @a: int a
 * @b: int b
 * Return: result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
